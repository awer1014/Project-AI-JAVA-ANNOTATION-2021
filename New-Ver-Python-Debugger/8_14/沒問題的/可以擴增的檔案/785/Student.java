public class Student { 
String id ; 
String name ; 
double score ; 
double sw ; 
Student ( String ii , String n , double s , double ss ) { 
this . id = ii ; 
this . name = n ; 
this . score = s ; 
this . sw = ss ; 
} 
void setid ( String a ) { 
id = a ; 
} 
void setname ( String a ) { 
name = a ; 
} 
void setscore ( double a ) { 
score = a ; 
} 
void setsw ( double a ) { 
sw = a ; 
} 
String getid ( ) { 
return id ; 
} 
String getName ( ) { 
return name ; 
} 
double getscore ( ) { 
return score ; 
} 
double getsw ( ) { 
return sw ; 
} 
} 
